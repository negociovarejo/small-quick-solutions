    private void txtCodeKeyPressed(java.awt.event.KeyEvent evt)//GEN-FIRST:event_txtCodeKeyPressed
    {//GEN-HEADEREND:event_txtCodeKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            setCursor(ViewUtil.WAIT_CURSOR);
            try {
                Long code = Long.parseLong(txtCode.getText());
                customer = repository.find(code);

                if (customer != null) {
                    txtCustomer.setText(customer.getName());
                    txtTaxPayerRegistration.setText(customer.getTaxPayerRegistration());
                } else {
                    JOptionPane.showMessageDialog(this, "Cliente não encontrado!", "VR Software + Extension", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (NumberFormatException e) {
                MessageUtil.showException(this, "Campo Inválido!", e);
            }
            setCursor(ViewUtil.DEFAULT_CURSOR);
        }
    }//GEN-LAST:event_txtCodeKeyPressed

