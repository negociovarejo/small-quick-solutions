    private void txtTaxPayerRegistrationKeyPressed(java.awt.event.KeyEvent evt)//GEN-FIRST:event_txtTaxPayerRegistrationKeyPressed
    {//GEN-HEADEREND:event_txtTaxPayerRegistrationKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            saveChanges();
        }
    }//GEN-LAST:event_txtTaxPayerRegistrationKeyPressed

    private void saveChanges()
    {
        setCursor(ViewUtil.WAIT_CURSOR);
        if (customer != null) {
            customer.setTaxPayerRegistration(txtTaxPayerRegistration.getText());

            if (customer.getTaxPayerRegistration().length() != 18) {
                JOptionPane.showMessageDialog(this, "CNPJ Inválido!", "VR Software + Extension", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            try {
                repository.save(customer);
                JOptionPane.showMessageDialog(this, "Informações salvas com sucesso!", "VR Software + Extension", JOptionPane.INFORMATION_MESSAGE);
            } catch (RuntimeException e) {
                MessageUtil.showException(this, e);
            }
            txtCode.setText("");
            txtTaxPayerRegistration.setText("");
            txtCustomer.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um cliente primeiro!", "VR Software + Extension", JOptionPane.WARNING_MESSAGE);
        }
        setCursor(ViewUtil.DEFAULT_CURSOR);
    }

